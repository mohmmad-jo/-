const modal = document.getElementById("productModal");

function openModal(product) {

    modal.classList.add("active");

    document.getElementById("modalImage").src = product.image;
    document.getElementById("modalTitle").innerText = product.name[currentLang];
    document.getElementById("modalPrice").innerText = product.price;
    document.getElementById("modalDescription").innerText = product.description[currentLang];
    document.getElementById("modalUsage").innerText = product.usage[currentLang];

    const list = document.getElementById("modalBenefits");
    list.innerHTML = "";

    product.benefits[currentLang].forEach(b => {
        const li = document.createElement("li");
        li.textContent = b;
        list.appendChild(li);
    });

    document.getElementById("officialLink").href = product.link;
}

// close modal
document.getElementById("closeModal").addEventListener("click", () => {
    modal.classList.remove("active");
});

// click outside
modal.addEventListener("click", (e) => {
    if (e.target === modal) {
        modal.classList.remove("active");
    }
});

