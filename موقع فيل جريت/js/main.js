const container = document.getElementById("productsContainer");

function renderProducts() {

    container.innerHTML = "";

    products.forEach(product => {

        const card = document.createElement("div");
        card.className = "product-card";

        card.innerHTML = `
            <div class="product-image">
                <img loading="lazy" src="${product.image}">
                <button class="quick-view">${translations[currentLang].quickView}</button>
            </div>

            <div class="product-body">
                <h3>${product.name[currentLang]}</h3>
                <div class="product-price">${product.price}</div>
                <p>${product.description[currentLang]}</p>

                <div class="product-buttons">

                    <a href="${product.link}" target="_blank" class="primaryButton">
                        ${translations[currentLang].buyNow}
                    </a>

                    <a href="${getWhatsAppLink(product.name)}" target="_blank" class="secondaryButton">
                        ${translations[currentLang].whatsapp}
                    </a>

                </div>
            </div>
        `;

        card.querySelector(".quick-view").addEventListener("click", (e) => {
            e.stopPropagation();
            openModal(product);
        });

        container.appendChild(card);
    });
}

renderProducts();

